def game_loop(args):
    pygame.init()
    pygame.font.init()
    world = None
    original_settings = None

    try:
        client = carla.Client(args.host, args.port)
        client.set_timeout(20.0)

        sim_world = client.get_world()  # CLIENT
        # get_world(self):
        # Returns the world object currently active in the simulation.
        # This world will be later used for example to load maps.

        if args.sync:
            original_settings = sim_world.get_settings()
            settings = sim_world.get_settings()

            # set the world to synchronous mode
            if not settings.synchronous_mode:
                settings.synchronous_mode = True
                settings.fixed_delta_seconds = 0.02 
            sim_world.apply_settings(settings)

            traffic_manager = client.get_trafficmanager()
            traffic_manager.set_synchronous_mode(True)
        # set_synchronous_mode(self, mode_switch=True):
        # Sets the Traffic Manager to synchronous mode.
        # In a multiclient situation, only the TM-Server can tick.
        # Similarly, in a multiTM situation, only one TM-Server must tick.
        # Use this method in the client that does the world tick, and right after setting the world to synchronous mode,
        # to set which TM will be the master while in sync.

        if args.autopilot and not sim_world.get_settings().synchronous_mode:
            print("WARNING: You are currently in asynchronous mode and could "
                  "experience some issues with the traffic simulation")

        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)
        display.fill((0, 0, 0))
        pygame.display.flip()

        hud = HUD(args.width, args.height)  # HUD
        world = World(sim_world, hud, args)  # WORLD
        controller = KeyboardControl()  # CONTROL

        agent = CAN(world.player)

        # Set the agent destination
        spawn_points = world.map.get_spawn_points()
        destination = random.choice(spawn_points).location
        agent.set_destination(destination)

        clock = pygame.time.Clock()

        while True:
            clock.tick()
            if args.sync:
                world.world.tick()
            else:
                world.world.wait_for_tick()
            if controller.parse_events():
                return

            world.tick(clock)
            world.render(display)
            pygame.display.flip()

            if agent.done():
                if args.loop:
                    agent.set_destination(random.choice(spawn_points).location)
                    world.hud.notification("The target has been reached, searching for another target", seconds=4.0)
                    print("The target has been reached, searching for another target")
                else:
                    print("The target has been reached, stopping the simulation")
                    break
            
            agent._send_command()
            agent._get_control()
            control = agent.run()
            world.player.apply_control(control)


    finally:

        if original_settings:
            sim_world.apply_settings(original_settings)

        if (world and world.recording_enabled):
            client.stop_recorder()

        if world is not None:
            world.destroy()

        pygame.quit()


